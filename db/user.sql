CREATE TABLE `user` (
  `ID` int(8) NOT NULL COMMENT 'id',
  `USERNAME` varchar(32) NOT NULL COMMENT '用户名称',
  `NICK_NAME` varchar(32) DEFAULT NULL COMMENT '用户昵称',
  `PASSWORD` varchar(32) NOT NULL COMMENT '密码',
  `GENDER` tinyint(2) NOT NULL COMMENT '性别（0：女、1：男）',
  `BIRTHDAY` date DEFAULT NULL COMMENT '出生日期',
  `USER_LEVEL` varchar(8) NOT NULL COMMENT '用户等级',
  `MOBILE` int(13) DEFAULT NULL COMMENT '手机号',
  `WEIXIN_OPEN_ID` varchar(64) DEFAULT NULL COMMENT '微信开放id',
  `EMAIL_ADDR` varchar(32) DEFAULT NULL COMMENT '邮箱地址',
  `SESSION_KEY` varchar(64) DEFAULT NULL COMMENT '会话key',
  `PORTRAIT_URL` varchar(255) DEFAULT NULL COMMENT '头像url',
  `STATUS` varchar(8) NOT NULL COMMENT '用户状态：NORMAL正常、CANCEL注销、FROZEN冻结',
  `CREATOR_ID` int(6) NOT NULL COMMENT '创建者id',
  `CREATOR_NAME` varchar(32) NOT NULL COMMENT '创建者名称',
  `CREATE_TIME` timestamp(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6) ON UPDATE CURRENT_TIMESTAMP(6) COMMENT '创建时间',
  `UPDATER_ID` varchar(8) DEFAULT NULL COMMENT '更新者id',
  `UPDATER_NAME` varchar(32) DEFAULT NULL COMMENT '更新者名称',
  `UPDATE_TIME` datetime(6) DEFAULT NULL COMMENT '更新时间',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8